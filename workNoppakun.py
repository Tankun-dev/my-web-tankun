 # คำสั่งนำเข้าข้อมมูลของผู้ใช้
n1 = input("กรอกชื่อของคุณ: ")
n2 = input("กรอกอายุของคุณ: ")
K1 = float(input("กรุณากรอกน้ำหนักของคุณ (กิโลกรัม): "))
K2 = float(input("กรุณากรอกส่วนสูงของคุณ (เซนติเมตร): "))

# คำสั่งแสดงผลข้อมูลที่ผู้ใช้พิมพ์เข้ามา
print("ชื่อ:", n1)
print("อายุ:", n2)
print("น้ำหนัก:", K1, "กิโลกรัม")
print("ส่วนสูง:", K2, "เซนติเมตร")

def calculate_bmi(K1, K2):
    """คำนวณค่า BMI จากน้ำหนักและส่วนสูง"""
    return K1 / ((K2 / 100) ** 2)
bmi = K1 / ((K2 / 100) ** 2)

# ฟังก์ชัน คำสั่งเปรียบเทียบและแสดงผลลัพธ์ ตามค่า BMI
def get_bmi_status(bmi):
    """คืนค่าสถานะสุขภาพตามค่า BMI"""
    if bmi < 18.5:
        return "ผอม"
    elif bmi < 24.9:
        return "ปกติ"
    elif bmi < 29.9:
        return "น้ำหนักเกิน"
    else:
        return "อ้วน"
status = get_bmi_status(bmi)
# แสดงผลลัพธ์
print("\nสรุปข้อมูลของคุณ:")
print("ชื่อ:", n1)
print("อายุ:", n2)
print("\nค่า BMI ของคุณคือ:", round(bmi, 2))
print("สถานะสุขภาพ:", status) 

 
